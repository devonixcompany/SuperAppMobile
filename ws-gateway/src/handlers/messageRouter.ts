/**
 * ‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° OCPP ‡∏à‡∏≤‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• WebSocket
 * Parse OCPP message from WebSocket data
 * 
 * ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏á‡∏≤‡∏ô:
 * 1. ‡πÅ‡∏õ‡∏•‡∏á JSON string ‡πÄ‡∏õ‡πá‡∏ô array
 * 2. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° OCPP (‡∏ï‡πâ‡∏≠‡∏á‡πÄ‡∏õ‡πá‡∏ô array ‡∏ó‡∏µ‡πà‡∏°‡∏µ‡∏≠‡∏¢‡πà‡∏≤‡∏á‡∏ô‡πâ‡∏≠‡∏¢ 3 elements)
 * 3. ‡πÅ‡∏¢‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• messageTypeId, messageId, action ‡πÅ‡∏•‡∏∞ payload
 * 4. ‡∏™‡∏£‡πâ‡∏≤‡∏á OCPPMessage object ‡πÅ‡∏•‡∏∞‡∏™‡πà‡∏á‡∏Ñ‡∏∑‡∏ô
 */
export function parseOCPPMessage(data: string): OCPPMessage | null {
  try {
    // ‡πÅ‡∏õ‡∏•‡∏á JSON string ‡πÄ‡∏õ‡πá‡∏ô JavaScript object
    const parsed = JSON.parse(data);
    
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡πÄ‡∏õ‡πá‡∏ô array ‡πÅ‡∏•‡∏∞‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏≠‡∏¢‡πà‡∏≤‡∏á‡∏ô‡πâ‡∏≠‡∏¢ 3 elements
    if (!Array.isArray(parsed) || parsed.length < 3) {
      throw new Error('Invalid OCPP message format');
    }

    // ‡πÅ‡∏¢‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å array ‡∏ï‡∏≤‡∏°‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö OCPP
    const [messageTypeId, messageId, action, payload] = parsed;

    return {
      messageTypeId,
      messageId,
      action: messageTypeId === 2 ? action : undefined, // CALL message ‡∏°‡∏µ action
      payload: messageTypeId === 2 ? payload : action // ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö CALL payload ‡∏≠‡∏¢‡∏π‡πà‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏ó‡∏µ‡πà 4, ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö CALLRESULT/CALLERROR ‡∏≠‡∏¢‡∏π‡πà‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏ó‡∏µ‡πà 3
    };
  } catch (error) {
    console.error('Failed to parse OCPP message:', error);
    return null;
  }
}

/**
 * ‡∏à‡∏±‡∏î‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö OCPP
 * Format OCPP response message
 * 
 * ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° CALLRESULT ‡πÉ‡∏ô‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö: [3, messageId, payload]
 */
export function formatOCPPResponse(messageId: string, payload: any): string {
  // CALLRESULT message format: [3, messageId, payload]
  return JSON.stringify([3, messageId, payload]);
}

/**
 * ‡∏à‡∏±‡∏î‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î OCPP
 * Format OCPP error message
 * 
 * ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° CALLERROR ‡πÉ‡∏ô‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö: [4, messageId, errorCode, errorDescription, errorDetails]
 */
export function formatOCPPError(messageId: string, errorCode: string, errorDescription: string, errorDetails?: any): string {
  // CALLERROR message format: [4, messageId, errorCode, errorDescription, errorDetails]
  return JSON.stringify([4, messageId, errorCode, errorDescription, errorDetails || {}]);
}

/**
 * ‡∏à‡∏±‡∏î‡πÄ‡∏™‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° OCPP ‡πÑ‡∏õ‡∏¢‡∏±‡∏á handler ‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏°‡∏ï‡∏≤‡∏°‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ä‡∏±‡∏ô
 * Route OCPP message to appropriate version handler
 * 
 * ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏á‡∏≤‡∏ô:
 * 1. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° (‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡πÄ‡∏â‡∏û‡∏≤‡∏∞ CALL messages)
 * 2. ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å handler ‡∏ï‡∏≤‡∏°‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ä‡∏±‡∏ô OCPP
 * 3. ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÑ‡∏õ‡∏¢‡∏±‡∏á handler ‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏°
 * 4. ‡∏™‡πà‡∏á‡∏Ñ‡∏∑‡∏ô‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏´‡∏£‡∏∑‡∏≠‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î
 */
export async function routeMessage(routeInfo: RouteInfo): Promise<MessageResponse> {
  const { version, message, chargePointId } = routeInfo;
  
  try {
    console.log(`Routing ${message.action || 'response'} message for charge point ${chargePointId} using OCPP ${version}`);

    // ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡πÄ‡∏â‡∏û‡∏≤‡∏∞ CALL messages (messageTypeId = 2)
    if (message.messageTypeId !== 2) {
      console.log('Ignoring non-CALL message');
      return { success: true };
    }

    let response: any = null;

    // ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å handler ‡∏ï‡∏≤‡∏°‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ä‡∏±‡∏ô OCPP
    switch (version) {
      case '1.6':
      case 'OCPP16':
      case 'ocpp1.6':
        // ‡πÉ‡∏ä‡πâ handler ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö OCPP 1.6
        response = await handleV16Message(message.action!, message.payload, chargePointId, message.messageId);
        break;
      
      case '2.0':
      case '2.0.1':
      case 'OCPP20':
      case 'OCPP201':
      case 'ocpp2.0':
      case 'ocpp2.0.1':
        // ‡πÉ‡∏ä‡πâ handler ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö OCPP 2.0/2.0.1
        response = await handleV201Message(message.action!, message.payload);
        break;
      
      default:
        // ‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ä‡∏±‡∏ô‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö
        throw new Error(`Unsupported OCPP version: ${version}`);
    }

    return {
      success: true,
      response
    };

  } catch (error: any) {
    console.error('Message routing error:', error);
    return {
      success: false,
      error: {
        code: 'InternalError',
        description: error.message || 'Internal server error',
        details: error
      }
    };
  }
}

/**
 * ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° WebSocket ‡∏ó‡∏µ‡πà‡πÄ‡∏Ç‡πâ‡∏≤‡∏°‡∏≤
 * Handle incoming WebSocket message
 * 
 * ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏á‡∏≤‡∏ô:
 * 1. ‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏à‡∏≤‡∏Å WebSocket ‡πÄ‡∏õ‡πá‡∏ô OCPP message
 * 2. ‡∏™‡∏£‡πâ‡∏≤‡∏á RouteInfo ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡∏à‡∏±‡∏î‡πÄ‡∏™‡πâ‡∏ô‡∏ó‡∏≤‡∏á
 * 3. ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÑ‡∏õ‡∏¢‡∏±‡∏á message router
 * 4. ‡∏™‡πà‡∏á‡∏Å‡∏≤‡∏£‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏¢‡∏±‡∏á charge point (‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö CALL messages)
 */
export async function handleWebSocketMessage(
  data: string,           // ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏à‡∏≤‡∏Å WebSocket
  chargePointId: string,  // ‡∏£‡∏´‡∏±‡∏™ charge point
  ocppVersion: string,    // ‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ä‡∏±‡∏ô OCPP
  sendResponse: (message: string) => void  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏™‡πà‡∏á‡∏Å‡∏≤‡∏£‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö
): Promise<void> {
  // ‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏õ‡πá‡∏ô OCPP message

  const message = parseOCPPMessage(data);
 
  if (!message) {
    console.error('Failed to parse message from charge point:', chargePointId);
    return;
  }

  // ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡∏à‡∏±‡∏î‡πÄ‡∏™‡πâ‡∏ô‡∏ó‡∏≤‡∏á
  const routeInfo: RouteInfo = {
    source: chargePointId,
    destination: 'central_system',
    version: ocppVersion,
    message,
    chargePointId
  };

  // ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÑ‡∏õ‡∏¢‡∏±‡∏á router
  const result = await routeMessage(routeInfo);

  // ‡∏™‡πà‡∏á‡∏Å‡∏≤‡∏£‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏¢‡∏±‡∏á charge point ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö CALL messages
  if (message.messageTypeId === 2) {
    let responseMessage: string;

    console.log(`üîÑ Processing CALL message: ${message.action} from ${chargePointId}`);
    console.log(`üìä Route result - Success: ${result.success}, Response:`, result.response);

    if (result.success && result.response !== null) {
      // ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° CALLRESULT
      responseMessage = formatOCPPResponse(message.messageId, result.response);
      console.log(`‚úÖ Sending CALLRESULT for ${message.action} to ${chargePointId}:`, responseMessage);
    } else if (!result.success && result.error) {
      // ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° CALLERROR
      responseMessage = formatOCPPError(
        message.messageId,
        result.error.code,
        result.error.description,
        result.error.details
      );
      console.log(`‚ùå Sending CALLERROR for ${message.action} to ${chargePointId}:`, responseMessage);
    } else {
      // ‡∏Å‡∏≤‡∏£‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö
      responseMessage = formatOCPPResponse(message.messageId, {});
      console.log(`üì§ Sending empty CALLRESULT for ${message.action} to ${chargePointId}:`, responseMessage);
    }

    // ‡∏™‡πà‡∏á‡∏Å‡∏≤‡∏£‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏¢‡∏±‡∏á charge point
    console.log(`üöÄ About to send response to ${chargePointId} via sendResponse function`);
    sendResponse(responseMessage);
    console.log(`‚úÖ Response sent to ${chargePointId} for ${message.action}`);
  } else {
    console.log(`‚ÑπÔ∏è Message type ${message.messageTypeId} from ${chargePointId} does not require response`);
  }
}